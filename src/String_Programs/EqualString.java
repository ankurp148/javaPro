package String_Programs;

public class EqualString {

	public static void main(String[] args) 
	{
			// first string
				String s1 = "madam";
				// second string
				String s2 = "level";
				// check condition
				if (s1.equals(s2)) {
					System.out.println("Strings Are Equal");
				} else {
					System.out.println("Strings Are Not Equal");
				}

	}

}
